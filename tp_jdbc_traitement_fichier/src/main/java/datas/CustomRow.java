package datas;

public class CustomRow {
	
	private int id_Row;
	private String nom_Row;
	
	public CustomRow() {}

	public CustomRow(int id_Row, String nom_Row) {
		super();
		this.id_Row = id_Row;
		this.nom_Row = nom_Row;
	}

	public int getId_Row() {
		return id_Row;
	}

	public void setId_Row(int id_Row) {
		this.id_Row = id_Row;
	}

	public String getNom_Row() {
		return nom_Row;
	}

	public void setNom_Row(String nom_Row) {
		this.nom_Row = nom_Row;
	};
	
	
	
	
	

}
